function countTreesForSlope(slopeX, slopeY) {
  let x = slopeX
  let y = slopeY
  let result = 0

  while(y < input.length) {
    if (x >= input[y].length) x -= input[y].length

    if (input[y][x] === tree) {
      result++
    }

    x += slopeX
    y += slopeY
  }

  return result
}

